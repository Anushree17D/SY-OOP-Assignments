#include <iostream>
using namespace std;

class Student {
    string name;
    int roll;
public:
    void getData() {
        cout << "Enter student name: ";
        cin >> name;
        cout << "Enter roll number: ";
        cin >> roll;
    }

    void display() {
        cout << "\n--- Student Details ---" << endl;
        cout << "Name: " << name << endl;
        cout << "Roll Number: " << roll << endl;
    }
};

int main() {
    Student s;
    s.getData();
    s.display();
    return 0;
}
